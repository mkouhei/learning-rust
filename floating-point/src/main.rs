fn main() {
    let x = 2.0; // f32

    let y: f32 = 3.0; // f32

    // addition
    let sum = 5 + 10;

    // subtraction
    let difference = 95.5 - 4.3;

    // multiplication
    let production = 4 * 30;

    // division
    let quotient = 56.7 / 32.2;

    // remainder
    let remainder = 43 % 5;

    println!("f32: {}", x);
    println!("f32: {}", y);
    println!("addition: {}", sum);
    println!("substraction: {}", difference);
    println!("multiplication: {}", production);
    println!("division: {}", quotient);
    println!("remainder: {}", remainder);
}
